// Adição
console.log(2+5)

// Subtração
console.log(2-5)

// Multiplicação
console.log(2 * 5)

// Divisãoão
console.log(2 / 5)

// Incremento
let numero = 20
console.log(++numero)

// Decremento
numero = 25
console.log(--numero)

// Comparação simples compara apenas os valores/dados
console.log(5 == 5)
console.log(5 == "5")

// Comparação rigorosa além de comparar os valores, compara também o tipo de dado
console.log(5 === "5")

// Comparação de diferente
console.log(10 != 10)
console.log(10 != 5)

// Comparação de diferente rigida/rigorosa
console.log(10 !== 10)
console.log(10 !== "10")

// Comparar se maior
console.log(5 > 6)

// Comparar se maior ou igual
console.log(5 >= 6)

// Comparar se menor
console.log(5 < 6)

// Comparar se menor ou igual
console.log(5 <= 6)

// Operadores lógicos And no JS é &&
console.log(10>5 && 10>8)
console.log(10>5 && 10>8 && 10>11)

// Operadores lógicos Or no JS é ||
console.log(10>5 || 10>8)
console.log(10>5 || 10>8 || 10>11)

// Comparação de negação, basta acrescentar ! a qualquer operador de comparativo e ele inverte o resultado da operação
console.log(!10>5) // sabemos que 10 é maior que 5, mas vai retornar false porque usei o !

// Concatenar strings basta usar o (+)
console.log("Meu nome é" + " " + "Humberto Moraes")

